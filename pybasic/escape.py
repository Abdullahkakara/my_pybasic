txt = "This will insert one \\ (backslash)."
print(txt)

txt = "country \nBangladesh"    #\n = new line
print(txt)

txt = "country \tBangladesh"       #\t = tab
print(txt)

#This example erases one character (backspace):
txt = "country \bBangladesh"
print(txt) 

txt = "\110\157"    #to find octal value
print(txt)

txt = "We know that \"chittagong\" is the part of northen Bangladesh"
print(txt)
